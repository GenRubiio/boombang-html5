const tamaño = 30;

// Creamos la matriz de 30x30 llena de 1.
const matriz = Array.from({ length: tamaño }, () => Array(tamaño).fill(1));

// Array de posiciones a cambiar a 0.
// Ejemplo simple: para cambiar la posición [1,1] a 0.
const posiciones = [[14, 13], [15, 12]];

// Si quisieras crear la figura del ejemplo de salida, deberías incluir
// en el array todas las coordenadas que corresponden a los 0 mostrados.
// Por ejemplo:
// const posiciones = [
//   [6,5],[6,6],[6,7],[6,8],[6,9],[6,10],[6,11],[6,12],[6,13],[6,14],
//   [7,5],[7,6],[7,7],[7,8],[7,9],[7,10],[7,11],[7,12],[7,13],[7,14],[7,15],
//   ... // y así sucesivamente para cada fila que requieras modificar
// ];

// Recorrer cada posición para asignar el 0
posiciones.forEach(([fila, columna]) => {
    // Verificamos que la posición esté dentro de los límites de la matriz
    if (fila >= 0 && fila < tamaño && columna >= 0 && columna < tamaño) {
        matriz[fila][columna] = 0;
    }
});

// Imprimimos la matriz para ver el resultado
console.log(matriz);
